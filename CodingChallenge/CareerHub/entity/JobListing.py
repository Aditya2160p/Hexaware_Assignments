class JobListing:
    def __init__(self, job_id, company_id, title, description, location, salary, job_type, posted_date):
        self.job_id = job_id
        self.company_id = company_id
        self.title = title
        self.description = description
        self.location = location
        self.salary = salary
        self.job_type = job_type
        self.posted_date = posted_date